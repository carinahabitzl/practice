public class ArithmetischeOperatioren {
    public static void main(String[] args) {
        int zahl = 9;
        int zahl2 = 4;

        System.out.println("a + b = " + zahl + zahl2  );
        System.out.println("a - b = " + (zahl - zahl2));
        System.out.println("a * b =" + (zahl*zahl2));
        System.out.println("a / b = " + (zahl/zahl2));
        System.out.println("a % b = " + (zahl%zahl2));
}
}